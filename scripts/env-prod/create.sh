#!/bin/bash

# =====================================================================================================================================================================================================
# ===================================================================================== Creation Namespace PROD =======================================================================================
# =====================================================================================================================================================================================================

echo "================================================================================================================== Creating namespace ..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/namespace.yaml



# =====================================================================================================================================================================================================
# ======================================================================================= Postgres DB ENV PROD ========================================================================================
# =====================================================================================================================================================================================================

echo "================================================================================================================== Creating postgres configmap..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/postgres-configmap.yaml

echo "================================================================================================================== Creating postgres pv volume..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/postgres-pv-volum.yaml

echo "================================================================================================================== Creating postgres pv claim..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/postgres-pv-claim.yaml

echo "================================================================================================================== Creating postgres deployment..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/postgres-deployment.yaml

echo "================================================================================================================== Creating postgres service..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/postgres-service.yaml

echo "================================================================================================================== Waiting for postgres DB ..."
sleep 35s



# =====================================================================================================================================================================================================
# ====================================================================================== user-service ENV PROD ========================================================================================
# =====================================================================================================================================================================================================

echo "================================================================================================================== Creating user-service deployment..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/user-service-deployment.yaml

echo "================================================================================================================== Creating user-service service..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/user-service-service.yaml



# =====================================================================================================================================================================================================
# ===================================================================================== Creation Ingress PROD =========================================================================================
# =====================================================================================================================================================================================================

echo "================================================================================================================== Creating ingress ..."
kubectl create -f ~/Projects/pet-project-based-on-quarkus-framework/k8s/env-prod/ingress-resource.yaml
